package main.java.core.method_declaraions;

public class Exercise {

	void myMethod() {
		System.out.println("void myMethod()");
	}

	public void myMethod2() {
		System.out.println("public void myMethod2()");
	}

	public void main() {
		System.out.println("public void main()");
	}

	public static void main(int x) {
		System.out.println("public static void main(int x)");
	}

	public static void main(String args[]) {
		System.out.println("public static void main(String args[])"); // public static void main(String args[])
	}
}